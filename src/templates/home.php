    </span><span class="main">— <em>The CAPjournal is</em> <em>a free peer-reviewed journal for astronomy communicators, online and  in print</em><br />
    <br />
    Public communication of astronomy provides  an important link between the scientific astronomical community and society,  giving visibility to scientific success stories and supporting both formal and  informal science education. While the principal task of an astronomer is to  further our knowledge of the Universe, disseminating this new information to a  wider audience than the scientific community is becoming increasingly  important. This is the main task of public astronomy communication — to bring  astronomy to society. <br />
        <br />
        The <a href="http://www.astronomy2009.org/" target="_blank">International Year of Astronomy 2009</a> was a unique platform to  inform the public about the latest discoveries in astronomy as well as to  emphasize the essential role of astronomy in science education. However, as the  astronomy outreach community expanded globally, it became increasingly  important to establish a community of science communication experts. <br />
        <br />
        The IAU  DIVISION C <a href="http://www.communicatingastronomy.org/" target="_blank">Commission C2 Communicating Astronomy with the Public</a> Journal  Working Group prepared a study assessing the feasibility of the Communicating  Astronomy with the Public Journal (CAPjournal). The conclusions were  inescapable. The present situation of public astronomy communication shows a  clear need for a publication addressing the specific needs of the public  astronomy communication community.</span><br />
